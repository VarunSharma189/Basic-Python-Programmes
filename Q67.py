# Program to Perform Set Operations:

set1 = {1, 2, 3}
set2 = {3, 4, 5}
union = set1 | set2
intersection = set1 & set2
difference = set1 - set2
print("Union:", union) # Outputs: {1, 2, 3, 4, 5}
print("Intersection:", intersection) # Outputs: {3}
print("Difference:", difference) # Outputs: {1, 2}
